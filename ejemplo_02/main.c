// Programa que muetra valores de variables integer, float char y double

#include <stdio.h>
#include <stdlib.h>

int main()
{
    int integerA = 2022;
    float floatA = 3.141593;
    char letterA = '@';
    double doubleA = 10.11;



    printf("El entero es: %i \n", integerA );
    printf("El flotante es: %f \n", floatA );
    printf("El caracter es: %c \n", letterA );
    printf("El double es: %f \n", doubleA );

    return 0;
}
