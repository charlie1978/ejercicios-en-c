// Programa que muetra valores de variables integer, float y un caracter
#include <stdio.h>
#include <stdlib.h>
#include <stdbool.h>
int main()
{
    int integerA = 321;
    float floatA = 3.141593;
    char letterA = '@';

    printf("El entero es: %i \n", integerA );
    printf("El flotante es: %f \n", floatA );
    printf("El caracter es: %c \n", letterA );

    return 0;
}
